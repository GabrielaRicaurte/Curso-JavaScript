// inicializar una variable //
var producto = 'Monitor de 24 pulgadas';
console.log(producto);

// las variables se pueden reasignar //
producto = 'Monitor de 50 pulgadas';
console.log(producto);

// javaScript es un lenguaje de tipo dinamico, no se especifica datos //
producto = 20;
console.log(producto);

// se puede inicializar sin un valor y asignar despues //
var disponible;
disponible = true;

